-- Delete Artwork, Artist, Materials, Gallery, and TicketPrice tables
drop table Artwork;
drop table NewArt;
drop table Artist;
drop table Materials;
drop table Gallery;
drop table TicketPrice;
drop table Building;
drop table Membership;
drop sequence materialsID_seq;

-- Create the 5 Tables with constraints

create table Materials(
    materialsID number(5),
    category varchar2(20),
    medium varchar2(50),
    Constraint Materials_PK primary key (materialsID)
);

create table TicketPrice(
    year number(4),
    ticketPrice number(3),
    Constraint TicketPrice_PK primary key (year),
    -- Ticket prices must be $50 or more
    Constraint TicketPrice_ticketPrice check (ticketPrice >= 50)
);

create table Building(
    buildingName varchar2(20),
    street varchar2(40),
    city varchar2(20),
    state char(2),
    zipcode char(5),
    Constraint Building_PK primary key (buildingName),
    Constraint Building_zipcode check (zipcode IS NOT NULL)
);

create table Gallery(
    gallery varchar2(20),
    floor char(1),
    building varchar2(20),
    Constraint Gallery_PK primary key (gallery),
    Constraint Gallery_building_FK Foreign Key (building) References Building (buildingName),
    Constraint Gallery_floor check (floor in ('1', '2', '3', '4', '5', 'B', 'O'))
);

create table Membership(
    memberLevel varchar2(15),
    yearlyFee number(4),
    discount number(2,2),
    Constraint Membership_PK primary key (memberLevel),
    -- Discount cannot be 20% or more
    Constraint Membership_discount check (discount < 20)
);


create table Artist(
    artistID number(5),
    firstName varchar2(20),
    lastName varchar2(25),
    city varchar2(20),
    state char(2),
    memberLevel varchar2(15),
    Constraint Artist_PK primary key (artistID),
    Constraint Artist_memberLevel_FK Foreign Key (memberLevel) References Membership(memberLevel),
    --Artists may only be from New England states (Massachusetts, New Hampshire, Vermont, Connecticut, Maine, and Rhode Island), the state of New York, or the West Coast states (California, Oregon, and Washington)
    Constraint Artist_state check (state in ('MA', 'NH', 'VT', 'CT', 'ME', 'RI', 'NY', 'CA', 'OR', 'WA')) 
);

create table Artwork(
    year number(4),
    artworkID number(5),
    artistID number(5),
    title varchar2(75),
    materialsID number(5),
    price number(5),
    chosen number(3) NULL,
    purchased char(1),
    purchaseDate date,
    gallery varchar2(20),
    -- Foreign keys referencing the 4 other tables
    -- Combination of year and artworkID is primary key
    Constraint Artwork_PK primary key (year, artworkID),

    Constraint Artwork_artistID_FK Foreign Key (artistID) References Artist (artistID),
    Constraint Artwork_materialsID_FK Foreign Key (materialsID) References Materials (materialsID),
    Constraint Artwork_galleryID_FK Foreign Key (gallery) References Gallery (gallery),
    Constraint Artwork_year_FK Foreign Key (year) References TicketPrice (year),
    --Purchased field can only be 'y' or 'n'
    Constraint Artwork_purchased check (purchased in ('y','n'))
);

create table NewArt(
    year number(4),
    artworkID number(5),
    artistID number(5),
    title varchar2(75),
    materialsID number(5),
    price number(5),
    chosen number(3) NULL,
    purchased char(1),
    purchaseDate date,
    gallery varchar2(20),
    -- Foreign keys referencing the 4 other tables
    -- Combination of year and artworkID is primary key
    Constraint NewArt_PK primary key (year, artworkID),

    Constraint NewArt_artistID_FK Foreign Key (artistID) References Artist (artistID),
    Constraint NewArt_materialsID_FK Foreign Key (materialsID) References Materials (materialsID),
    Constraint NewArt_FK Foreign Key (gallery) References Gallery (gallery),
    Constraint NewArt_year_FK Foreign Key (year) References TicketPrice (year),
    --Purchased field can only be 'y' or 'n'
    Constraint NewArt_purchased check (purchased in ('y','n'))
);

